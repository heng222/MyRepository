GM_BOOL GM_PD_Build_Table(GM_UINT32 XORPara, GM_UINT32* pPDTable)
{
    GM_UINT32 i       = 0U;
    int j             = 0;
    GM_UINT32 data   = 0U;
    GM_UINT32 result = 0U;
    GM_UINT32 temp = 0U;

    if (NULL == pPDTable)
    {
        return GM_FALSE;
    }

    for (i = 0U; i < GM_PD_TABLE_SIZE; ++i)
    {
        data = (GM_UINT32)(i << 23U);

        result = data;

        for (j = 0; j < 9; ++j)
        {
            result <<= 1U;

            temp = data & 0x80000000;

            if ((GM_UINT32)0U == (GM_UINT32)temp)
            {
                result = ~(result ^ XORPara);
            }
            else
            {
                result |= (GM_UINT32)0x01;
            }

            data = result;
        }

        pPDTable[i] = data;
    }

    return GM_TRUE;
}


GM_BOOL GM_LFSR_Build_Table(GM_UINT32 polynomial, GM_UINT32 pTable[], GM_BOOL isLeft)
{
    GM_UINT32 val   = 0U;
    GM_UINT32 i     = 0U;
    GM_UINT32 j     = 0U;
    GM_UINT32 carry = 0U;
    GM_UINT32 mmask = 0U;

    if (NULL == pTable)
    {
        return GM_FALSE;
    }

    /** @brief PD查表用的左移表 */
    if (GM_TRUE == isLeft)
    {
        for (j = (GM_UINT32)0u; j < (GM_UINT32)256u; ++j)
        {
            val = j;
            val <<= 24U;
            mmask = (GM_UINT32)0u;

            for (i = (GM_UINT32)0u; i < (GM_UINT32)8u; ++i)
            {
                carry = val & ((GM_UINT32)0x80000000);
                val <<= 1U;
                mmask <<= 1U;

                if ((GM_UINT32)0U != (GM_UINT32)carry)
                {
                    val ^= polynomial;
                    mmask ^= polynomial;
                }
            }

            pTable[j] = mmask;
        }
    }
    else /**< PD查表用的右移表 */
    {
        for (j = (GM_UINT32)0u; j < (GM_UINT32)256u; ++j)
        {
            val = j;
            val = (GM_UINT32)(val);
            mmask = (GM_UINT32)0u;

            for (i = (GM_UINT32)0u; i < (GM_UINT32)8; ++i)
            {
                carry = val & ((GM_UINT32)0x00000001);

                if ((GM_UINT32)0U != (GM_UINT32)carry)
                {
                    val ^= polynomial;
                    mmask ^= polynomial;
                }

                val >>= 1U;

                mmask >>= 1U;

                if ((GM_UINT32)0U != (GM_UINT32)carry)
                {
                    val |= (GM_UINT32)0x80000000;
                    mmask |= (GM_UINT32)0x80000000;
                }
            }

            pTable[j] = mmask;
        }
    }

    return GM_TRUE;
}


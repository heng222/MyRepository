GM_BOOL GM_CRC_Build_CRC32_Table(GM_UINT32 polynomial, GM_UINT32 pCRCTable[])
{
    GM_UINT32 temp = 0U;
    int i          = 0U;
    int j          = 0U;
    GM_UINT32 t1   = 0U;
    GM_UINT32 t2   = 0U;
    GM_UINT32 flag = 0U;

    if (NULL == pCRCTable)
    {
        return GM_FALSE;
    }

    for (i = 0U; i < 256U; ++i)
    {
        temp = (GM_UINT32)GM_CRC_Reflect((GM_UINT32)i, 8U);

        pCRCTable[i] = temp << 24U;

        for (j = 0U; j < 8U; ++j)
        {
            flag = pCRCTable[i] & (GM_UINT32)0x80000000U;
            t1 = (pCRCTable[i] << 1U);

            if ((GM_UINT32)0U == (GM_UINT32)flag)
            {
                t2 = (GM_UINT32)0u;
            }
            else
            {
                t2 = polynomial;
            }

            pCRCTable[i] = t1 ^ t2;
        }

        pCRCTable[i] = (GM_UINT32)(GM_CRC_Reflect((GM_UINT32)(pCRCTable[i]), 32U));
    }

    return GM_TRUE;
}

